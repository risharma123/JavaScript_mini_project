const yearElement= document.querySelector('.year-box')
const monthElement= document.querySelector('.month-box')
const dayElement= document.querySelector('.day-box')
const daynumElement= document.querySelector('.day-num')
let date=new Date()
yearElement.innerHTML=date.getFullYear()
monthElement.innerHTML=date.toLocaleString('en',{month:'long'})
daynumElement.innerHTML=date.getDate()
dayElement.innerHTML=date.toLocaleString('en',{weekday:'long'})